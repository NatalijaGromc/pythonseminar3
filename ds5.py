N = int(input('Введите число N '))
arr = [0,1]
arr2 = [0,1]
for i in range(2,N+1):
    arr.append(arr[i-2]+arr[i-1])
    arr2.append(arr2[i-2]-arr2[i-1])
print (arr2[::-1]+arr[1:])
